// Open/Close Functions
function openFile(id) {
    const data = fileData[id];
    document.getElementById('detail-title').innerText = data.title;
    document.getElementById('detail-id').innerText = data.id;
    document.getElementById('detail-img').src = data.img;
    document.getElementById('detail-description').innerHTML = data.desc;
    document.getElementById('file-detail-view').classList.add('show');
}

function closeFile() {
    document.getElementById('file-detail-view').classList.remove('show');
}

// Startup
window.onload = () => {
    setTimeout(() => {
        const overlay = document.getElementById('startup-overlay');
        overlay.style.transition = 'opacity 0.8s';
        overlay.style.opacity = '0';
        setTimeout(() => { overlay.style.display = 'none'; }, 800);
    }, 2500);
};

// Clock
setInterval(() => {
    const now = new Date();
    const clock = document.getElementById('clock');
    if (clock) clock.textContent = now.getHours().toString().padStart(2, '0') + ":" + 
                                   now.getMinutes().toString().padStart(2, '0') + ":" + 
                                   now.getSeconds().toString().padStart(2, '0');
}, 1000);